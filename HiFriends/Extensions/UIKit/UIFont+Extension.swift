import Foundation
import UIKit

extension UIFont {
    
    static func helvetica18() -> UIFont? {
        return UIFont.init(name: "Helvetica", size: 18.0)
    }
    static func helvetica20() -> UIFont? {
        return UIFont.init(name: "Helvetica", size: 20.0)
    }
    static func helvetica26() -> UIFont? {
        return UIFont.init(name: "Helvetica", size: 26.0)
    }
}
